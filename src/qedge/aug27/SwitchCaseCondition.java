package qedge.aug27;

public class SwitchCaseCondition {

	public static void main(String[] args) {
		// print switch case condition
		String Book="novel";
		switch (Book) {
		case "meesho":
			System.out.println( "Book is ready");
			
			break;
		case "amazon":
			System.out.println("Book is ready");
			break;
		case "novel":
			System.out.println("Book is ready");
			
			break;
		default:
			System.out.println("Book is not ready");
			break;
		}

	}

}
