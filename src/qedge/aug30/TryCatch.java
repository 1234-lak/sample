public class TryCatch {

	public static void main(String[] args) {
		// divide anything with zero
		try {
		int x=2345,y=0,z;
		z=x/y;
		System.out.println(z);
		}catch(Throwable t)
		{System.out.println(t.getMessage());
		
		}
		//division
		double a=2454,b=45,c;
		c=a/b;
		System.out.println(c);
		}
		
		

	}


